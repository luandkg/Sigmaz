package Sigmaz.S05_Executor;

public class TipoAssociativo {

    private String mTipo1;
    private String mTipo2;

    public TipoAssociativo(String eTipo1,String eTipo2){
        mTipo1=eTipo1;
        mTipo2=eTipo2;
    }

    public String getTipo1(){return mTipo1;}
    public String getTipo2(){return mTipo2;}

}
